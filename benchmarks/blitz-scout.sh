#!/usr/bin/env bash
# ═══════════════════════════════════════════════════════════════════
# Blitz Scout — Mass conflict discovery across GitHub
# ═══════════════════════════════════════════════════════════════════
# Builds a seed list of 50+ PRs with active merge conflicts.
# Output: benchmarks/blitz-targets.json + benchmarks/blitz-targets.md
# ═══════════════════════════════════════════════════════════════════

SCOUT_DIR=$(mktemp -d)
TARGETS_JSON="/Users/zacharyholwerda/Desktop/Airlock/repos/noconflict/benchmarks/blitz-targets.json"
TARGETS_MD="/Users/zacharyholwerda/Desktop/Airlock/repos/noconflict/benchmarks/blitz-targets.md"

G='\033[0;32m'; Y='\033[1;33m'; R='\033[0;31m'
DIM='\033[2m'; BOLD='\033[1m'; RST='\033[0m'

echo ""
echo -e "${BOLD}  ╔═══════════════════════════════════════════╗${RST}"
echo -e "${BOLD}  ║     BLITZ SCOUT — Build the Hit List      ║${RST}"
echo -e "${BOLD}  ╚═══════════════════════════════════════════╝${RST}"
echo ""

LANGUAGES=("typescript" "javascript" "python" "go" "rust" "java" "ruby" "swift" "kotlin" "c++")
MIN_STARS=20
REPOS_PER_LANG=50

TOTAL_FOUND=0
TOTAL_SCANNED=0

# Init JSON
echo "[" > "${TARGETS_JSON}"
FIRST_JSON=true

# Init markdown
cat > "${TARGETS_MD}" << 'MDEOF'
# Blitz Targets — NoConflict Outreach

> Conflicted PRs found by ConflictScout. Ready for outreach.

| # | Repo | Stars | PR | Title | Severity | Conflicts | Lines | Language |
|---|------|------:|-------|-------|----------|----------:|------:|----------|
MDEOF

for lang in "${LANGUAGES[@]}"; do
  echo -e "  ${BOLD}── ${lang} ──${RST}"

  # Get repos
  REPOS=$(gh search repos --language "${lang}" --stars ">=${MIN_STARS}" --sort updated --limit "${REPOS_PER_LANG}" --json fullName,stargazersCount --jq '.[] | "\(.fullName)|\(.stargazersCount)"' 2>/dev/null || echo "")

  [ -z "${REPOS}" ] && continue

  LANG_FOUND=0

  while IFS='|' read -r repo stars; do
    [ -z "${repo}" ] && continue

    # Get conflicting PRs
    PRS=$(gh pr list --repo "${repo}" --state open --limit 10 --json number,headRefName,baseRefName,title,mergeable --jq '.[] | select(.mergeable == "CONFLICTING") | "\(.number)|\(.headRefName)|\(.baseRefName)|\(.title)"' 2>/dev/null || echo "")

    [ -z "${PRS}" ] && continue

    while IFS='|' read -r pr_num head_ref base_ref pr_title; do
      [ -z "${pr_num}" ] && continue
      TOTAL_SCANNED=$((TOTAL_SCANNED+1))

      # Quick severity estimate without cloning (save time for the seed list)
      # We'll do full analysis during blitz execution
      # For now, use GitHub's conflict marker as proof + get changed files count
      changed_files=$(gh pr view "${pr_num}" --repo "${repo}" --json changedFiles --jq '.changedFiles' 2>/dev/null || echo "0")

      # Estimate severity from file count
      sev="yellow"
      if [ "${changed_files}" -gt 50 ]; then
        sev="red"
      elif [ "${changed_files}" -le 3 ]; then
        sev="green"
      fi

      sev_upper=$(echo "${sev}" | tr 'a-z' 'A-Z')

      case "${sev}" in
        green)  color="${G}" ;;
        yellow) color="${Y}" ;;
        red)    color="${R}" ;;
      esac

      # Truncate title
      short_title="${pr_title:0:55}"

      echo -e "    ${color}${sev_upper}${RST} ${repo}#${pr_num} ${DIM}(${changed_files} files) ${short_title}${RST}"

      TOTAL_FOUND=$((TOTAL_FOUND+1))
      LANG_FOUND=$((LANG_FOUND+1))

      # Write to JSON
      if [ "${FIRST_JSON}" = "true" ]; then
        FIRST_JSON=false
      else
        echo "," >> "${TARGETS_JSON}"
      fi

      # Escape title for JSON
      safe_title=$(echo "${pr_title}" | sed 's/"/\\"/g' | sed "s/'/\\\\'/g")

      cat >> "${TARGETS_JSON}" << JSONEOF
  {
    "repo": "${repo}",
    "stars": ${stars},
    "pr": ${pr_num},
    "title": "${safe_title}",
    "head": "${head_ref}",
    "base": "${base_ref}",
    "changed_files": ${changed_files},
    "estimated_severity": "${sev}",
    "language": "${lang}",
    "url": "https://github.com/${repo}/pull/${pr_num}"
  }
JSONEOF

      # Write to markdown
      echo "| ${TOTAL_FOUND} | [${repo}](https://github.com/${repo}) | ${stars} | [#${pr_num}](https://github.com/${repo}/pull/${pr_num}) | ${short_title} | ${sev_upper} | - | ${changed_files} | ${lang} |" >> "${TARGETS_MD}"

    done <<< "${PRS}"
  done <<< "${REPOS}"

  echo -e "  ${DIM}  found ${LANG_FOUND} in ${lang}${RST}"
  echo ""
done

# Close JSON
echo "" >> "${TARGETS_JSON}"
echo "]" >> "${TARGETS_JSON}"

# Add summary to markdown
cat >> "${TARGETS_MD}" << MDEOF

## Summary

- **Total PRs found:** ${TOTAL_FOUND}
- **Repos scanned:** ${TOTAL_SCANNED}+
- **Languages:** ${LANGUAGES[*]}

## Blitz Execution

Run the blitz:
\`\`\`bash
./benchmarks/blitz-fire.sh          # comments on all targets
./benchmarks/blitz-fire.sh --dry    # preview without commenting
\`\`\`

---
*Generated by [NoConflict](https://github.com/airlock-labs/noconflict)*
MDEOF

echo ""
echo -e "  ${BOLD}─── SEED LIST COMPLETE ───${RST}"
echo ""
echo -e "  ${G}${TOTAL_FOUND}${RST} conflicted PRs found"
echo -e "  ${DIM}json: benchmarks/blitz-targets.json${RST}"
echo -e "  ${DIM}markdown: benchmarks/blitz-targets.md${RST}"
echo ""
