#!/usr/bin/env bash
# ═══════════════════════════════════════════════════════════════════
# ConflictScout — Find real merge conflicts on GitHub
# ═══════════════════════════════════════════════════════════════════
#
# Searches GitHub for PRs with merge conflicts, clones them locally,
# runs the severity router, and generates an outreach report.
#
# Usage:
#   ./benchmarks/conflict-scout.sh [--lang typescript] [--stars 100] [--limit 10]
#
# Requires: gh (GitHub CLI), git, jq
# ═══════════════════════════════════════════════════════════════════

LANG_FILTER="${1:-typescript}"
MIN_STARS="${2:-50}"
LIMIT="${3:-10}"
SCOUT_DIR=$(mktemp -d)
REPORT_FILE="${SCOUT_DIR}/scout-report.md"

G='\033[0;32m'; Y='\033[1;33m'; R='\033[0;31m'
DIM='\033[2m'; BOLD='\033[1m'; RST='\033[0m'

echo ""
echo -e "${BOLD}  ╔═══════════════════════════════════════════╗${RST}"
echo -e "${BOLD}  ║     CONFLICT SCOUT — NoConflict           ║${RST}"
echo -e "${BOLD}  ╚═══════════════════════════════════════════╝${RST}"
echo ""
echo -e "${DIM}  hunting: language=${LANG_FILTER} stars>=${MIN_STARS} limit=${LIMIT}${RST}"
echo -e "${DIM}  workspace: ${SCOUT_DIR}${RST}"
echo ""

# ─── Severity classification (same as conflict-bench.sh) ─────────
classify() {
  local dir="$1"
  local base_branch="$2"
  local pr_branch="$3"
  cd "${dir}" 2>/dev/null || return

  local has_conflict="false"
  local conflict_count=0
  local total_lines=0

  # merge-tree dry run
  if ! git merge-tree --write-tree "${pr_branch}" "${base_branch}" >/dev/null 2>&1; then
    has_conflict="true"
    conflict_count=$(git merge-tree --write-tree "${pr_branch}" "${base_branch}" 2>&1 | grep -c "CONFLICT" 2>/dev/null || echo "0")
  fi

  # count changed lines
  total_lines=$(git diff --numstat "${base_branch}...${pr_branch}" 2>/dev/null | awk '{s+=$1+$2} END {print s+0}' 2>/dev/null || echo "0")

  # trivial check
  local trivial_only
  trivial_only=$(git diff "${base_branch}...${pr_branch}" 2>/dev/null | awk '
    /^\+\+\+/ || /^---/ { next }
    /^[+-]/ {
      line = substr($0, 2)
      gsub(/^[ \t]+/, "", line)
      gsub(/[ \t]+$/, "", line)
      if (length(line) == 0) next
      if (line ~ /^import /) next
      if (line ~ /^export /) next
      if (line ~ /^\/\//) next
      if (line ~ /^\/\*/) next
      if (line ~ /^\*/) next
      if (line == "{" || line == "}") next
      found = 1
      print "false"
      exit
    }
    END { if (!found) print "true" }
  ')

  local sev="green"
  if [ "${has_conflict}" = "true" ]; then
    if [ "${trivial_only}" = "true" ]; then
      sev="green"
    elif [ "${total_lines}" -gt 50 ] || [ "${conflict_count}" -gt 2 ]; then
      sev="red"
    else
      sev="yellow"
    fi
  fi

  # Get conflicting file list
  local conflict_files=""
  if [ "${has_conflict}" = "true" ]; then
    conflict_files=$(git merge-tree --write-tree "${pr_branch}" "${base_branch}" 2>&1 | grep "CONFLICT" | head -5 || echo "")
  fi

  echo "${sev}|${conflict_count}|${total_lines}|${conflict_files}"
}

# ─── Step 1: Find PRs with merge conflicts ───────────────────────
echo -e "  ${BOLD}PHASE 1: Hunting for conflicted PRs...${RST}"
echo ""

# Search for repos with recent PRs in the target language
# Then check each repo's PRs for mergeable state
FOUND=0
SCANNED=0
GREEN_COUNT=0
YELLOW_COUNT=0
RED_COUNT=0

# Start the report
cat > "${REPORT_FILE}" << 'MDEOF'
# ConflictScout Report

> Generated by NoConflict — murder your merge conflicts.

## Findings

| Repo | PR | Severity | Conflicts | Lines Changed | Files |
|------|----|---------:|----------:|--------------:|-------|
MDEOF

# Search for PRs that are open and have merge conflicts
# GitHub API: mergeable_state can be "dirty" (has conflicts)
echo -e "  ${DIM}searching GitHub for open PRs...${RST}"

# Get popular repos with open PRs
REPOS=$(gh search repos --language "${LANG_FILTER}" --stars ">=${MIN_STARS}" --sort stars --limit 30 --json fullName --jq '.[].fullName' 2>/dev/null || echo "")

if [ -z "${REPOS}" ]; then
  echo -e "  ${R}no repos found. check gh auth status.${RST}"
  exit 1
fi

for repo in ${REPOS}; do
  [ "${FOUND}" -ge "${LIMIT}" ] && break

  # Get open PRs for this repo (check mergeable state)
  PRS=$(gh pr list --repo "${repo}" --state open --limit 5 --json number,headRefName,baseRefName,title,mergeable --jq '.[] | select(.mergeable == "CONFLICTING") | "\(.number)|\(.headRefName)|\(.baseRefName)|\(.title)"' 2>/dev/null || echo "")

  [ -z "${PRS}" ] && continue

  while IFS='|' read -r pr_num head_ref base_ref pr_title; do
    [ "${FOUND}" -ge "${LIMIT}" ] && break
    [ -z "${pr_num}" ] && continue

    SCANNED=$((SCANNED+1))

    echo -e "  ${DIM}[${SCANNED}] ${repo}#${pr_num}: ${pr_title:0:60}${RST}"

    # Clone the repo (shallow)
    local_dir="${SCOUT_DIR}/repos/${repo//\//-}-${pr_num}"
    if git clone -q --depth 50 "https://github.com/${repo}.git" "${local_dir}" 2>/dev/null; then
      cd "${local_dir}"

      # Fetch the PR branch
      git fetch -q origin "pull/${pr_num}/head:pr-${pr_num}" 2>/dev/null || continue
      git fetch -q origin "${base_ref}" 2>/dev/null || continue

      # Run severity classification
      result=$(classify "${local_dir}" "origin/${base_ref}" "pr-${pr_num}")
      sev=$(echo "${result}" | cut -d'|' -f1)
      conflicts=$(echo "${result}" | cut -d'|' -f2)
      lines=$(echo "${result}" | cut -d'|' -f3)
      files=$(echo "${result}" | cut -d'|' -f4-)

      # Color output
      case "${sev}" in
        green)  color="${G}"; GREEN_COUNT=$((GREEN_COUNT+1)) ;;
        yellow) color="${Y}"; YELLOW_COUNT=$((YELLOW_COUNT+1)) ;;
        red)    color="${R}"; RED_COUNT=$((RED_COUNT+1)) ;;
      esac

      echo -e "    ${color}$(echo "${sev}" | tr 'a-z' 'A-Z')${RST} — ${conflicts} conflicts, ${lines} lines changed"

      # Shorten files for table
      files_short=$(echo "${files}" | head -3 | sed 's/CONFLICT.*: //' | tr '\n' ', ' | sed 's/,$//')

      # Add to report
      echo "| [${repo}](https://github.com/${repo}) | [#${pr_num}](https://github.com/${repo}/pull/${pr_num}) | $(echo "${sev}" | tr 'a-z' 'A-Z') | ${conflicts} | ${lines} | ${files_short:0:80} |" >> "${REPORT_FILE}"

      FOUND=$((FOUND+1))
    fi
  done <<< "${PRS}"
done

echo ""

# ─── Results ──────────────────────────────────────────────────────
echo -e "  ${BOLD}─── SCOUT RESULTS ───${RST}"
echo ""
echo -e "  ${DIM}scanned:${RST} ${SCANNED} PRs across $(echo "${REPOS}" | wc -l | tr -d ' ') repos"
echo -e "  ${DIM}found:${RST}   ${FOUND} conflicted PRs"
echo ""
echo -e "  ${G}${GREEN_COUNT} GREEN${RST}  ${Y}${YELLOW_COUNT} YELLOW${RST}  ${R}${RED_COUNT} RED${RST}"
echo ""

# Add summary to report
cat >> "${REPORT_FILE}" << MDEOF

## Summary

- **Scanned:** ${SCANNED} PRs
- **Conflicted:** ${FOUND} PRs
- **GREEN:** ${GREEN_COUNT} (trivial — NoConflict auto-resolves)
- **YELLOW:** ${YELLOW_COUNT} (semantic — NoConflict explains + asks)
- **RED:** ${RED_COUNT} (dangerous — NoConflict hard-stops)

## Outreach Template

For YELLOW/RED PRs, consider commenting:

\`\`\`
Hey! Noticed this PR has merge conflicts. We're building a tool called
NoConflict that classifies and auto-resolves git conflicts.

Your PR has ${conflicts} conflict(s) — our engine classifies this as
[SEVERITY]. Here's what it sees:

[paste analysis]

If you want to try it: npm i -g noconflict && nc push

Free during beta. Would love your feedback.
\`\`\`

---
*Generated by [NoConflict](https://github.com/airlock-labs/noconflict) — murder your merge conflicts.*
MDEOF

echo -e "  ${DIM}report: ${REPORT_FILE}${RST}"

# Copy report to repo
FINAL_REPORT="/Users/zacharyholwerda/Desktop/Airlock/repos/noconflict/benchmarks/scout-report.md"
cp "${REPORT_FILE}" "${FINAL_REPORT}" 2>/dev/null && echo -e "  ${DIM}saved: benchmarks/scout-report.md${RST}"
echo ""

# Cleanup clones (keep report)
rm -rf "${SCOUT_DIR}/repos"
